const mongoose = require("mongoose");

const sellerUserSchema = new mongoose.Schema({
    userid: {
        type: String,
        required: true,
        unique: true,
    },
    username: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
});

module.exports = mongoose.model("selleruser", sellerUserSchema);
